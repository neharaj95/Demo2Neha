package programming_Basic;

public class Swapping {

	public static void main(String[] args) {
		
		int firstNumber=5;
		int secondNumber=6;
		int temp=0;
		
		temp=firstNumber;
		firstNumber=secondNumber;
		secondNumber=temp;
		
		System.out.println("First number is " +firstNumber + " and second number is "+secondNumber);
		
				

	}

}
